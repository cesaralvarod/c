#include <stdio.h>

int main() {
    // This is a comment

    /*
    This is
    a multi-line
    comment
    */

    /*
    * Escape sequences = character combination of a backslash \
                    followed or by a letter or combination of digits.
    \n -> new line
    \t -> new tab
    */

    printf("I \nlike\t pizza\n");
    printf("Quotes\" ^-^");

    return 0;
}
